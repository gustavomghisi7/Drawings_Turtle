from turtle import *

joana = Turtle()
alice = Turtle()
angulo = 90

alice.color('red')
alice.forward(150)
alice.left(angulo)
alice.forward(100)
alice.left(angulo)
alice.forward(100)
alice.left(angulo)
alice.forward(100)

joana.color('green')
joana.forward(150)
joana.right(angulo)
joana.forward(150)
joana.right(angulo)
joana.forward(150)
joana.right(angulo)
joana.forward(150)

done()
